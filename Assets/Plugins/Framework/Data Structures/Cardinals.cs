using UnityEngine;
using System.Collections;

/// <summary>
/// Enum representing one of the four cardinal directions.
/// </summary>
public enum Cardinal4
{
    //It is important that these are in clockwise order
    North = 0,
    East = 1,
    South = 2,
    West = 3,
}

/// <summary>
/// Enum representing one of the eight cardinal directions.
/// </summary>
public enum Cardinal8
{
    //It is important that these are in clockwise order
    North,
    NorthEast,
    East,
    SouthEast,
    South,
    SouthWest,
    West,
    NorthWest
}


